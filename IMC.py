import tkinter as tk
from tkinter import messagebox
import os

caminhoDoScript = os.path.dirname(os.path.abspath(__file__))
os.chdir(caminhoDoScript)

def calcularImc():
    try:
        peso = float(entryPeso.get())
        altura = float(entryAltura.get()) / 100
        imc = peso / (altura ** 2)
        labelResultado.config(text=f'IMC: {imc:.2f}  Kg/m2')

        if imc < 18.5:
            imagemPath = 'imc_magro.png'
        elif imc < 24.9:
            imagemPath = 'imc_normal.png'
        elif imc < 29.9:
            imagemPath = 'imc_sobrepeso.png'
        elif imc < 39.9:
            imagemPath = 'imc_obeso.png'
        else:
            imagemPath = 'imc_obeso_2.png'

        imagem = tk.PhotoImage(file=imagemPath)
        labelImagem.config(image=imagem)
        labelImagem.image = imagem
    
    except ValueError:
        messagebox.showerror('Erro', 'Por favor, insira valores válidos para peso e altura')

root = tk.Tk()
root.title('Calculadora de IMC')

framePrincipal = tk.Frame(root, padx=20, pady=20)
framePrincipal.pack()

labelTitulo = tk.Label(framePrincipal, text='Calculadora de IMC', font=('Arial', 18))
labelTitulo.grid(row=0, column=0, columnspan=2, pady=10)

labelPeso = tk.Label(framePrincipal, text='Peso')
labelPeso.grid(row=1, column=0, columnspan=2, pady=5, sticky='w')
entryPeso = tk.Entry(framePrincipal, width=10)
entryPeso.grid(row=1, column=1, pady=5)

labelAltura = tk.Label(framePrincipal, text='Altura (cm):')
labelAltura.grid(row=2, column=0, pady=5, sticky='w')
entryAltura = tk.Entry(framePrincipal, width=10)
entryAltura.grid(row=2, column=1, pady=5)

buttonCalcular = tk.Button(framePrincipal, text='Calcular IMC', command=calcularImc)
buttonCalcular.grid(row=3, column=0, columnspan=2, pady=10)

labelResultado = tk.Label(framePrincipal, text='', font=('Arial', 14))
labelResultado.grid(row=4, column=0, columnspan=2, pady=10)

labelImagem = tk.Label(framePrincipal)
labelImagem.grid(row=5, column=0, columnspan=2)

